public class Assembler {
	
	//Converts inputs from decimal into binary
	public int BinaryConverter(int val){
		int binary = 0000;
		
		return binary;
	}
	//Converts inputs from binary into hexadecimal outputs
	public String HexConverter(int val){
		String hex = "0x00000";
		
		return hex;
	}
}
